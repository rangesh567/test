package day3;

public class UpperCase {
	public static void main(String[] args) {
		String name1="Satheesh";
		String name2="Rangesh";
		String name3="Hariharan";
		String cName=name1.toUpperCase();
		System.out.println(cName);
		String c2Name=name2.toUpperCase();
		System.out.println(c2Name);
		String c3Name=name3.toUpperCase();
		System.out.println(c3Name);
	} 
	

}
