package day3;

public class LowerCase {
	public static void main(String[] args) {
		String name1="Satheesh";
		String name2="Rangesh";
		String name3="Hariharan";
		String sName=name1.toLowerCase();
		System.out.println(sName);
		String s2Name=name2.toLowerCase();
		System.out.println(s2Name);
		String s3Name=name3.toLowerCase();
		System.out.println(s3Name);
	}

}
